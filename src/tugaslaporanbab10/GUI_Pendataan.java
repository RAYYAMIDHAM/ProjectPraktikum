    public void insert() {
        String nama = (String) cmbNama.getSelectedItem();
        String jaminan = (String) cmbBarang.getSelectedItem();
        String pengembalian = txtPengembalian.getText();
        String kondisi;
        if (radioRusak.isSelected()){
            kondisi = "R";
        }else{
            kondisi = "A";
        }
        try {
            koneksi();
            Statement statement = conn.createStatement();
            statement.executeUpdate("INSERT INTO gui_pendataan(Nama, Jenis_Alat, Rusak/Aman, Pengembalian)"
                    + "VALUES('" + nama + "','" + jaminan + "','" + kondisi + "','" + pengembalian + "')");
            statement.close();
            JOptionPane.showMessageDialog(null, "Berhasil Memasukan Data Nilai!");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Terjadi Kesalahan Input!");
        }
        refresh();
    }

